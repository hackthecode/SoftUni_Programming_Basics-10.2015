using System;

class DeclareVariables
{
    static void Main()
    {
        // Declare Variables
        ushort value1 = 52130;
        sbyte value2 = -115;
        int value3 = 4825932;
        byte value4 = 97;
        short value5 = -10000;
        // Console Print 
        Console.WriteLine(("ushort <--> ") + value1);
        Console.WriteLine(("sbyte <--> ") + value2);
        Console.WriteLine(("int <--> ") + value3);
        Console.WriteLine(("byte <--> ") + value4);
        Console.WriteLine(("short <--> ") + value5);
    }
}
