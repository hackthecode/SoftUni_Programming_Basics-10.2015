using System;

class FloatOrDouble
{
    static void Main()
    {
        // Declare Variables
        double value1 = 34.567839023;
        float value2 = 12.345f;
        double value3 = 8923.1234857;
        float value4 = 3456.091f;
        // Console Print 
        Console.WriteLine(("double <--> ") + value1);
        Console.WriteLine(("float <--> ") + value2);
        Console.WriteLine(("double <--> ") + value3);
        Console.WriteLine(("float <--> ") + value4);
    }
}
