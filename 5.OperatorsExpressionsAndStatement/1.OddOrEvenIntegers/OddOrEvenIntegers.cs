using System;

namespace _1.OddOrEvenIntegers
{
    class OddOrEvenIntegers
    {
        static void Main()
        {
            Console.WriteLine("Check if given integer is odd or even");
            int number = int.Parse(Console.ReadLine());
            bool isOdd = number % 2 != 0;
            Console.WriteLine(isOdd);
        }
    }
}
